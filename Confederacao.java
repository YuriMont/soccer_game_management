public class Confederacao {
    private String pais;
    private Liga liga;
    private Copa copa;

    Confederacao(){}
    
    public String getPais() {
        return pais;
    }
    public void setPais(String pais) {
        this.pais = pais;
    }
    public Liga getLiga() {
        return liga;
    }
    public void setLiga(Liga liga) {
        this.liga = liga;
    }
    public Copa getCopa() {
        return copa;
    }
    public void setCopa(Copa copa) {
        this.copa = copa;
    }

    
}
